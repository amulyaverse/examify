'use client';
import { useState, useEffect } from 'react';
import { useParams, useRouter } from 'next/navigation';

interface ResultData {
  _id: string;
  obtainedScore: number;
  totalScore: number;
  correctAnswers: number;
  wrongAnswers: number;
  percentage: number;
  timeTaken: number;
  subjectWiseScore?: Record<string, { correct: number; total: number; score: number }>;
  answers?: Record<string, string>;
}

interface ExamData {
  _id: string;
  title: string;
  questions: Array<{
    id: string;
    question: string;
    options: string[];
    correctAnswer: string;
    marks: number;
  }>;
}

export default function ResultPage() {
  const params = useParams();
  const router = useRouter();
  const [result, setResult] = useState<ResultData | null>(null);
  const [exam, setExam] = useState<ExamData | null>(null);
  const [loading, setLoading] = useState(true);

  useEffect(() => {
    fetchResult();
  }, [params.id]);

  const fetchResult = async () => {
    try {
      const response = await fetch(`/api/result/${params.id}`);
      const data = await response.json();
      setResult(data.result);
      setExam(data.exam);
      setLoading(false);
    } catch (err) {
      console.error('Error loading result:', err);
      setLoading(false);
    }
  };

  const downloadResult = () => {
    const content = `
═══════════════════════════════════════
         EXAMIFY - EXAM RESULT
═══════════════════════════════════════

Exam: ${exam?.title}
Date: ${new Date().toLocaleDateString()}
Time: ${new Date().toLocaleTimeString()}

───────────────────────────────────────
                    SCORE SUMMARY
───────────────────────────────────────

Total Score: ${result?.obtainedScore} / ${result?.totalScore}
Percentage: ${result?.percentage?.toFixed(2)}%
Correct Answers: ${result?.correctAnswers}
Wrong Answers: ${result?.wrongAnswers}
Time Taken: ${Math.floor((result?.timeTaken || 0) / 60)} minutes ${(result?.timeTaken || 0) % 60} seconds

───────────────────────────────────────
              PERFORMANCE
───────────────────────────────────────

Accuracy: ${((result?.correctAnswers || 0) / (result?.correctAnswers! + result?.wrongAnswers!) * 100 || 0).toFixed(1)}%

═══════════════════════════════════════
    Generated by Examify Platform
═══════════════════════════════════════
    `;
    
    const blob = new Blob([content], { type: 'text/plain' });
    const url = URL.createObjectURL(blob);
    const a = document.createElement('a');
    a.href = url;
    a.download = `exam-result-${exam?.title}.txt`;
    a.click();
    URL.revokeObjectURL(url);
  };

  const printResult = () => {
    window.print();
  };

  if (loading) {
    return (
      <div className="min-h-screen flex items-center justify-center">
        <div className="text-center">
          <div className="animate-spin rounded-full h-12 w-12 border-b-2 border-blue-600 mx-auto"></div>
          <p className="mt-4 text-gray-600">Loading results...</p>
        </div>
      </div>
    );
  }

  if (!result || !exam) {
    return (
      <div className="min-h-screen flex items-center justify-center">
        <div className="text-center">
          <h2 className="text-2xl font-bold text-red-600">Result not found</h2>
          <button onClick={() => router.push('/dashboard')} className="mt-4 bg-blue-600 text-white px-4 py-2 rounded">
            Go to Dashboard
          </button>
        </div>
      </div>
    );
  }

  const accuracy = (result.correctAnswers / (result.correctAnswers + result.wrongAnswers)) * 100 || 0;

  return (
    <div className="min-h-screen bg-gradient-to-b from-blue-50 to-white py-8">
      <div className="container mx-auto px-4 max-w-4xl">
        {/* Header */}
        <div className="text-center mb-8">
          <div className="text-6xl mb-4">🎉</div>
          <h1 className="text-3xl font-bold text-gray-800">Exam Completed!</h1>
          <p className="text-gray-600 mt-2">{exam.title}</p>
        </div>

        {/* Score Card */}
        <div className="bg-white rounded-lg shadow-lg p-8 mb-6 text-center">
          <div className="inline-block relative">
            <div className="w-40 h-40 rounded-full border-8 border-blue-100 flex items-center justify-center mx-auto">
              <div>
                <div className="text-4xl font-bold text-blue-600">
                  {result.percentage.toFixed(0)}%
                </div>
                <div className="text-sm text-gray-500">Score</div>
              </div>
            </div>
          </div>
          
          <div className="mt-4">
            <div className="text-3xl font-bold text-gray-800">
              {result.obtainedScore} / {result.totalScore}
            </div>
            <p className="text-gray-500 mt-1">Total Score</p>
          </div>
        </div>

        {/* Statistics Grid */}
        <div className="grid grid-cols-2 md:grid-cols-4 gap-4 mb-6">
          <div className="bg-white rounded-lg shadow p-4 text-center">
            <div className="text-2xl font-bold text-green-600">{result.correctAnswers}</div>
            <div className="text-sm text-gray-600">Correct</div>
          </div>
          <div className="bg-white rounded-lg shadow p-4 text-center">
            <div className="text-2xl font-bold text-red-600">{result.wrongAnswers}</div>
            <div className="text-sm text-gray-600">Wrong</div>
          </div>
          <div className="bg-white rounded-lg shadow p-4 text-center">
            <div className="text-2xl font-bold text-blue-600">{accuracy.toFixed(1)}%</div>
            <div className="text-sm text-gray-600">Accuracy</div>
          </div>
          <div className="bg-white rounded-lg shadow p-4 text-center">
            <div className="text-2xl font-bold text-purple-600">
              {Math.floor(result.timeTaken / 60)}:{String(result.timeTaken % 60).padStart(2, '0')}
            </div>
            <div className="text-sm text-gray-600">Time Taken</div>
          </div>
        </div>

        {/* Performance Message */}
        <div className="bg-white rounded-lg shadow p-6 mb-6 text-center">
          <p className="text-lg">
            {result.percentage >= 80 && "🏆 Excellent performance! Keep up the great work!"}
            {result.percentage >= 60 && result.percentage < 80 && "👍 Good job! You're doing well."}
            {result.percentage >= 40 && result.percentage < 60 && "📚 Keep practicing! You can do better."}
            {result.percentage < 40 && "💪 Don't give up! Review and try again."}
          </p>
        </div>

        {/* Action Buttons */}
        <div className="flex gap-4">
          <button
            onClick={downloadResult}
            className="flex-1 bg-blue-600 text-white py-3 rounded-lg hover:bg-blue-700 transition-colors"
          >
            📥 Download Result
          </button>
          <button
            onClick={printResult}
            className="flex-1 border border-blue-600 text-blue-600 py-3 rounded-lg hover:bg-blue-50 transition-colors"
          >
            🖨️ Print Result
          </button>
          <button
            onClick={() => router.push('/dashboard')}
            className="flex-1 bg-gray-600 text-white py-3 rounded-lg hover:bg-gray-700 transition-colors"
          >
            📊 Dashboard
          </button>
        </div>
      </div>
    </div>
  );
}